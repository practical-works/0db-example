//------------------------------------------------------------------------------
// ● Clear-Console
//------------------------------------------------------------------------------
console.cls = function () {
  const readline = require("readline");
  console.log("\n".repeat(process.stdout.rows));
  readline.cursorTo(process.stdout, 0, 0);
  readline.clearScreenDown(process.stdout);
};
